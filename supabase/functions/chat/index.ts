import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are an expert tourism guide for Andhra Pradesh, India. You help travelers plan trips, suggest destinations, provide cost estimates, share cultural insights, and give travel tips.

Key destinations you know about:
- Araku Valley (Eco - hill station, coffee plantations, tribal culture)
- Papikondalu (Eco - Godavari River cruise, gorges)
- Borra Caves (Eco - limestone caves, 150M years old)
- Tirupati (Cultural - Sri Venkateswara Temple, world's richest temple)
- Lepakshi (Cultural - hanging pillar, Vijayanagara architecture)
- Amaravati (Cultural - ancient Buddhist stupa, new capital)
- Rishikonda Beach (Coastal - water sports, surfing)
- Yarada Beach (Coastal - secluded, pristine)

Be helpful, enthusiastic, and provide practical information including:
- Estimated costs (budget/mid/luxury)
- Best time to visit
- Travel duration from major cities
- Nearby hotels, restaurants, and attractions
- Cultural significance and history
- Eco-friendly travel tips

Keep responses concise but informative. Use emojis sparingly for warmth.`,
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Too many requests. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service temporarily unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
